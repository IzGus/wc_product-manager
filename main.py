    cache_file = Path("logs") / ".deps_check_cache"
    
    # –ï—Å–ª–∏ –∫—ç—à —Å—É—â–µ—Å—Ç–≤—É–µ—Ç –∏ –±—ã–ª —Å–æ–∑–¥–∞–Ω –º–µ–Ω–µ–µ —á–∞—Å–∞ –Ω–∞–∑–∞–¥, –∏—Å–ø–æ–ª—å–∑—É–µ–º –µ–≥–æ
